            mobileMenu.classList.toggle('hidden');
        });

        // Add event listener to the close button
        closeButton.addEventListener('click', function() {
            menuButton.setAttribute('aria-expanded', 'false');
            mobileMenu.classList.add('hidden');
        });
    });
